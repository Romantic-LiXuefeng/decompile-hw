    public static String[] getZhuyin(String hanzi, String pinyin, boolean isDialMap, boolean isOrig, Context context) {
        String[] ret = null;
        SparseArray<String[][]> mMultizhuyinMap = new SparseArray();
        if (!(hanzi == null || pinyin == null)) {
            int i;
            String[][] pinyinArray = new String[20][];
            mMultizhuyinMap.clear();
            int hanziCount = 0;
            int pinyinCount = 0;
            int count = -1;
            char[] hanziChar = hanzi.toCharArray();
            int hanziLen = hanzi.length();
            for (i = 0; i < hanziLen; i++) {
                char c = hanziChar[i];
                boolean isChinese = SortUtils.isChinese(c);
                if (isBlank(c, isOrig)) {
                    if (i > count + 1) {
                        hanziCount++;
                    }
                    count = i;
                } else if (isChinese) {
                    if (i > count + 1) {
                        hanziCount++;
                    }
                    hanziCount++;
                    Object multiPinyin = (String[][]) mMultiZhuyin.get(Character.valueOf(c));
                    if (multiPinyin != null) {
                        mMultizhuyinMap.put(hanziCount - 1, multiPinyin);
                    }
                    count = i;
                } else if (i == hanziLen - 1) {
                    hanziCount++;
                }
            }
            if (mMultizhuyinMap.size() == 0) {
                return new String[0];
            }
            int preBlankPos = -1;
            int preMultiPos = -1;
            int pinyinStrLen = pinyin.length();
            int arrayIndex = 0;
            char[] pinyinChar = pinyin.toCharArray();
            i = 0;
            while (i < pinyinStrLen) {
                boolean isCurCharBlank = isBlank(pinyinChar[i], isOrig);
                boolean isNextCharBlank = i < pinyinStrLen + -1 ? isBlank(pinyinChar[i + 1], isOrig) : !isCurCharBlank;
                if (isCurCharBlank && !r16) {
                    preBlankPos = i;
                    if (i == pinyinStrLen - 1) {
                        pinyinArray = SortUtils.addToStringArray(pinyinArray, SortUtils.addNewData(null, pinyin.substring(preMultiPos + 1, i + 1)), pinyinArray.length, arrayIndex);
                        arrayIndex++;
                    }
                } else if (!isCurCharBlank && r16) {
                    pinyinCount++;
                    String[][] multiPinyin2 = (String[][]) mMultizhuyinMap.get(pinyinCount - 1);
                    if (multiPinyin2 != null) {
                        if (!(preMultiPos == -1 && preBlankPos == -1)) {
                            pinyinArray = SortUtils.addToStringArray(pinyinArray, SortUtils.addNewData(null, pinyin.substring(preMultiPos + 1, preBlankPos + 1)), pinyinArray.length, arrayIndex);
                            arrayIndex++;
                        }
                        String currentPinyin = pinyin.substring(preBlankPos + 1, i + 1);
                        String[] aMultiPinyin = SortUtils.addNewData(null, currentPinyin);
                        for (int j = 0; j < multiPinyin2.length; j++) {
                            String data = "";
                            for (int k = 0; k < multiPinyin2[j].length; k++) {
                                if (isDialMap) {
                                    data = data + DialerHighlighter.convertToDialMapEX(multiPinyin2[j][k], context);
                                } else {
                                    data = multiPinyin2[j][k];
                                }
                            }
                            if (!currentPinyin.equalsIgnoreCase(data)) {
                                aMultiPinyin = SortUtils.addNewData(aMultiPinyin, data);
                            }
                        }
                        pinyinArray = SortUtils.addToStringArray(pinyinArray, aMultiPinyin, pinyinArray.length, arrayIndex);
                        arrayIndex++;
                        preMultiPos = i;
                    } else if (i == pinyinStrLen - 1) {
                        pinyinArray = SortUtils.addToStringArray(pinyinArray, SortUtils.addNewData(null, pinyin.substring(preMultiPos + 1, i + 1)), pinyinArray.length, arrayIndex);
                        arrayIndex++;
                    }
                }
                i++;
            }
            if (pinyinCount != hanziCount) {
                Log.e("MultiPinyin", "the size of name and pinyin not match, pinyinCount is " + pinyinCount + ", hanziCount is " + hanziCount);
                return new String[0];
            }
            ret = MultiPinyin.getCombination(SortUtils.expandStringArrayCapacity(pinyinArray, arrayIndex));
        }
        return ret;
    }

    private static boolean isBlank(char c, boolean isOrig) {
        boolean z = true;
        boolean z2 = false;
        if (isOrig) {
            if (!Character.isLetterOrDigit(c)) {
                z2 = true;
            }
            return z2;
        }
        if (c != ' ') {
            z = false;
        }
        return z;
    }
