package com.huawei.keyguard.amazinglockscreen.data;

public class Move {
    private int x;
    private int y;

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Move(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
